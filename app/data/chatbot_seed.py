async def seed_chatbot_questions(db_session: AsyncSession) -> None:
    existing = await db_session.execute(select(ChatbotQuestionTemplate.id).limit(1))
    if existing.first() is not None:
        return

    db_session.add_all([ChatbotQuestionTemplate(**template, is_active=True) for template in CHATBOT_TEMPLATES])
    await db_session.commit()
